const mongoose = require("mongoose");
const { Schema } = mongoose;

const ThemeSchema = new Schema({
  name: {
    type: String,
    required: true,
    unique: true,
  },
  slug: {
    type: String,
    required: true,
    unique: true,
  },
  image: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
});

const Theme = mongoose.model("Theme", ThemeSchema);
module.exports = Theme;
